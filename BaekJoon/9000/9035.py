# 정수 n이 주어졌을 때, 1,2,3의 합으로 나타내는 경우의 수
# 합을 나타낼 때는 수를 1개 이상 사용해야 함.

# 생각할 수 있는 것 :
# 작은 문제로 쪼개는법?
# dp에 저장되어 있지 않다면

T = int(input())
dp = [0] * (12)
dp[0] = 0
dp[1] = 1

for _ in range(T):
    n = int(input())
    if n >= 2:
        dp[2] = 2
    if n >= 3:
        dp[3] = 4
    for i in range(4, n+1):
        dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3]
    print(dp[n])
